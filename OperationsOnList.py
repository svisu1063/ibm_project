list=[]
list.append(20)
list.append(22)
list.append(20)
print(list)



#insert an integer at specified position
list.insert(1,21)
print(list)



#print the list
print("List: ")
print(list)



#remove duplicate integers from list
print("\nList after removing duplicate element:")
list.remove(20)
print(list)


#insert an interger at the end of the list
list.append(23)
print("\nList after adding integer at last:")
print(list)



#sort the list
list.sort()
print("\nList in sorted order:")
print(list)




#pop the last element from the list
list.pop()
print("\nList after pop operation:")
print(list)




#reverse the list
list.reverse()
print("\nList in reversed order:")
print(list)





